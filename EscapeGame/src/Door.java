import java.util.*;
import java.awt.*;
import java.awt.event.*;

public class Door {
    
    private int doorX = 350;
    private int doorY = 100;
    
    public Door()
    {
        
    }

    public int getDoorX() {
        return doorX;
    }

    public void setDoorX(int doorX) {
        this.doorX = doorX;
    }

    public int getDoorY() {
        return doorY;
    }

    public void setDoorY(int doorY) {
        this.doorY = doorY;
    }
    
}
