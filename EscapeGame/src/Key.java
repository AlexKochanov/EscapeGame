import java.util.*;
import java.awt.*;
import java.awt.event.*;

public class Key {
    
    private int keyX = 750;
    private int keyY = 750;
    
    public Key()
    {
        
    }

    public int getKeyX() {
        return keyX;
    }

    public void setKeyX(int keyX) {
        this.keyX = keyX;
    }

    public int getKeyY() {
        return keyY;
    }

    public void setKeyY(int keyY) {
        this.keyY = keyY;
    }
    
}
